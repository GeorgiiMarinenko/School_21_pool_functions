int	ft_is_prime(int nb)
{
	int	i;

	i = 2;
	if (nb <= 1)
		return (0);
	while (i < nb)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}

int	ft_find_next_prime(int nb)
{
	int	i;

	i = 1;
	if (ft_is_prime(nb) == 1)
		return (nb);
	else
		while (ft_is_prime(nb + i) == 0)
			i++;
	return (nb + i);
}
