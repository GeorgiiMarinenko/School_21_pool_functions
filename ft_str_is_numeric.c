int		ft_str_is_numeric(char *str)
{
	unsigned int	flag;

	flag = 0;
	if (str[0] == '\0')
	{
		flag = 1;
	}
	while (*str)
	{
		if ((*str < '0') || (*str > '9'))
			return (0);
		else
			flag = 1;
		++str;
	}
	return (flag);
}
