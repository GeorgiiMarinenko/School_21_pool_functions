int		ft_str_is_alpha(const char *str)
{
	int	flag;

	flag = 0;
	if (str[0] == '\0')
	{
		flag = 1;
	}
	while (*str)
	{
		if ((*str < 'A' || *str > 'Z')
				&& (*str < 'a' || *str > 'z'))
			return (0);
		else
			flag = 1;
		++str;
	}
	return (flag);
}
